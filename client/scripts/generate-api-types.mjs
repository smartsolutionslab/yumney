/**
 * Generates TypeScript types from committed OpenAPI specs.
 *
 * Reads each JSON under client/libs/shared/api-client/openapi/ and writes
 * a matching .ts file under client/libs/shared/api-client/src/lib/generated/
 * using openapi-typescript.
 *
 * Usage: yarn generate:api-types
 */
import { readdir, mkdir, writeFile, access } from 'node:fs/promises';
import { join, resolve, dirname, basename, extname } from 'node:path';
import { fileURLToPath } from 'node:url';
import openapiTS, { astToString } from 'openapi-typescript';

const __dirname = dirname(fileURLToPath(import.meta.url));
const specsDir = resolve(__dirname, '..', 'libs', 'shared', 'api-client', 'openapi');
const outDir = resolve(__dirname, '..', 'libs', 'shared', 'api-client', 'src', 'lib', 'generated');

async function exists(path) {
  try {
    await access(path);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  if (!(await exists(specsDir))) {
    console.error(`Specs directory missing: ${specsDir}`);
    process.exit(1);
  }

  const entries = (await readdir(specsDir)).filter((f) => extname(f) === '.json');

  if (entries.length === 0) {
    console.warn(
      `No specs in ${specsDir}. See openapi/README.md for how to populate them.`,
    );
    return;
  }

  await mkdir(outDir, { recursive: true });

  const indexLines = [
    '// Auto-generated by scripts/generate-api-types.mjs. Do not edit by hand.',
    '',
  ];

  for (const file of entries) {
    const moduleName = basename(file, '.json');
    const specPath = join(specsDir, file);
    const outPath = join(outDir, `${moduleName}.ts`);

    const ast = await openapiTS(new URL(`file://${specPath.replace(/\\/g, '/')}`), {
      exportType: true,
    });
    const contents =
      '// Auto-generated from openapi/' + file + '. Do not edit by hand.\n' +
      '/* eslint-disable */\n' +
      astToString(ast);

    await writeFile(outPath, contents, 'utf8');
    indexLines.push(`export * as ${moduleName} from './${moduleName}';`);
    console.log(`  ${moduleName}: ${specPath} -> ${outPath}`);
  }

  await writeFile(join(outDir, 'index.ts'), indexLines.join('\n') + '\n', 'utf8');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
