/**
 * Generates frontend validation constants from backend domain value objects.
 *
 * Reads public const declarations from src/Yumney.*.Domain/ C# files
 * and writes a TypeScript constants file that the frontend can import.
 *
 * Usage: node scripts/generate-validation-constants.mjs
 */
import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..', '..');
const srcDir = join(repoRoot, 'src');
const outputPath = join(__dirname, '..', 'libs', 'shared', 'models', 'src', 'lib', 'validation-constants.ts');

const MODULE_PATTERN = /Yumney\.(\w+)\.Domain$/;
const CLASS_PATTERN = /public\s+sealed\s+record\s+(\w+)/;
const CONST_PATTERN = /^\s*public\s+const\s+(int|string)\s+(\w+)\s*=\s*(.+?)\s*;\s*$/gm;
const MESSAGE_SUFFIX = /Message$/;

function toScreamingSnake(name) {
  return name
    .replace(/([a-z])([A-Z])/g, '$1_$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1_$2')
    .toUpperCase();
}

function parseValue(type, raw) {
  if (type === 'int') {
    return parseInt(raw, 10);
  }
  const match = raw.match(/"([^"]*)"/);
  return match ? `'${match[1]}'` : raw;
}

async function discoverDomainProjects() {
  const entries = await readdir(srcDir, { withFileTypes: true });
  return entries
    .filter((e) => e.isDirectory() && MODULE_PATTERN.test(e.name))
    .map((e) => ({
      module: e.name.match(MODULE_PATTERN)[1].toUpperCase(),
      path: join(srcDir, e.name),
    }));
}

async function findCsFiles(dir) {
  const entries = await readdir(dir, { recursive: true, withFileTypes: true });
  return entries
    .filter((e) => e.isFile() && e.name.endsWith('.cs') && !e.parentPath.includes('obj'))
    .map((e) => join(e.parentPath, e.name));
}

async function extractConstants(filePath) {
  const content = await readFile(filePath, 'utf-8');

  const classMatch = content.match(CLASS_PATTERN);
  if (!classMatch) {
    return null;
  }

  const className = classMatch[1];
  const constants = {};

  let match;
  CONST_PATTERN.lastIndex = 0;
  while ((match = CONST_PATTERN.exec(content)) !== null) {
    const [, type, name, rawValue] = match;
    if (MESSAGE_SUFFIX.test(name)) {
      continue;
    }
    constants[toScreamingSnake(name)] = parseValue(type, rawValue);
  }

  if (Object.keys(constants).length === 0) {
    return null;
  }

  return { className: toScreamingSnake(className), constants };
}

function generateTypeScript(modules) {
  const lines = [
    '/**',
    ' * ---------------------------------------------------------------',
    ' * AUTO-GENERATED FILE — DO NOT EDIT MANUALLY',
    ' *',
    ' * Generated by: scripts/generate-validation-constants.mjs',
    ' * Source: src/Yumney.*.Domain/ value objects',
    ' * Run: yarn generate:validation',
    ' * ---------------------------------------------------------------',
    ' */',
    '',
    'export const VALIDATION = {',
  ];

  const sortedModules = Object.keys(modules).sort();
  for (const mod of sortedModules) {
    lines.push(`  ${mod}: {`);
    const sortedClasses = Object.keys(modules[mod]).sort();
    for (const cls of sortedClasses) {
      const consts = modules[mod][cls];
      const entries = Object.entries(consts)
        .sort(([a], [b]) => a.localeCompare(b))
        .map(([k, v]) => `${k}: ${v}`)
        .join(', ');
      lines.push(`    ${cls}: { ${entries} },`);
    }
    lines.push('  },');
  }

  lines.push('} as const;');
  lines.push('');

  return lines.join('\n');
}

async function main() {
  const projects = await discoverDomainProjects();
  const modules = {};

  for (const project of projects) {
    const files = await findCsFiles(project.path);
    for (const file of files) {
      const result = await extractConstants(file);
      if (result) {
        if (!modules[project.module]) {
          modules[project.module] = {};
        }
        modules[project.module][result.className] = result.constants;
      }
    }
  }

  const output = generateTypeScript(modules);
  await writeFile(outputPath, output, 'utf-8');
  console.log(`✓ Generated validation constants → ${outputPath}`);
}

main().catch((err) => {
  console.error('Failed to generate validation constants:', err);
  process.exit(1);
});
